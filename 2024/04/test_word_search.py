from pytest import mark


def _count_matches(rows: list[str]) -> int:
    return sum(row.count("XMAS") for row in rows)


def _produce_top_left_to_bottom_right_diagonals(left_to_right: list[str]) -> list[str]:
    start_x = 0
    start_y = len(left_to_right) - 1
    output = list[str]()

    while start_x < len(left_to_right[0]):
        chars = []

        x = start_x
        y = start_y

        while x < len(left_to_right[0]) and y < len(left_to_right):
            chars.append(left_to_right[y][x])
            x += 1
            y += 1

        output.append("".join(chars))

        if start_x == 0 and start_y > 0:
            start_y -= 1
        else:
            start_x += 1

    return output


def _produce_bottom_left_to_top_right_diagonals(left_to_right: list[str]) -> list[str]:
    start_x = len(left_to_right[0]) - 1
    start_y = len(left_to_right) - 1
    output = list[str]()

    while start_y >= 0:
        chars = []

        x = start_x
        y = start_y

        while x < len(left_to_right[0]) and y >= 0:
            chars.append(left_to_right[y][x])
            x += 1
            y -= 1

        output.append("".join(chars))

        if start_y == len(left_to_right) - 1 and start_x > 0:
            start_x -= 1
        else:
            start_y -= 1

    return output


def _produce_top_to_bottom(left_to_right: list[str]) -> list[str]:
    return ["".join(row[column_index] for row in left_to_right) for column_index in range(len(left_to_right[0]))]


def word_search1(input_str: str) -> int:
    left_to_right = input_str.splitlines()
    right_to_left = [line[::-1] for line in left_to_right]
    top_to_bottom = _produce_top_to_bottom(left_to_right)
    bottom_to_top = [line[::-1] for line in top_to_bottom]
    top_left_to_bottom_right_diagonals = _produce_top_left_to_bottom_right_diagonals(left_to_right)
    bottom_right_to_top_left_diagonals = [line[::-1] for line in top_left_to_bottom_right_diagonals]
    bottom_left_to_top_right_diagonals = _produce_bottom_left_to_top_right_diagonals(left_to_right)
    top_right_to_bottom_left_diagonals = [line[::-1] for line in bottom_left_to_top_right_diagonals]

    return (
        _count_matches(left_to_right)
        + _count_matches(right_to_left)
        + _count_matches(top_to_bottom)
        + _count_matches(bottom_to_top)
        + _count_matches(top_left_to_bottom_right_diagonals)
        + _count_matches(bottom_right_to_top_left_diagonals)
        + _count_matches(bottom_left_to_top_right_diagonals)
        + _count_matches(top_right_to_bottom_left_diagonals)
    )


def word_search2(input_str: str) -> int:
    count = 0
    left_to_right = input_str.splitlines()
    for y in range(1, len(left_to_right) - 1):
        for x in range(1, len(left_to_right[y]) - 1):
            if (
                left_to_right[y][x] == "A"
                and (
                    (left_to_right[y - 1][x - 1] == "M" and left_to_right[y + 1][x + 1] == "S")
                    or (left_to_right[y - 1][x - 1] == "S" and left_to_right[y + 1][x + 1] == "M")
                )
                and (
                    (left_to_right[y + 1][x - 1] == "M" and left_to_right[y - 1][x + 1] == "S")
                    or (left_to_right[y + 1][x - 1] == "S" and left_to_right[y - 1][x + 1] == "M")
                )
            ):
                count += 1

    return count


